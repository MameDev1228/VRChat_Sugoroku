using UdonSharp;
using UnityEngine;
using VRC.SDKBase;
using VRC.Udon;

/// <summary>
/// ボードゲームの各マス（物件）を管理するクラス
/// ・購入価格や通行料、所有者情報を管理
/// ・エリアや増資レベルなどのゲームロジック情報も保持
/// </summary>
[UdonBehaviourSyncMode(BehaviourSyncMode.Manual)] // 手動同期（必要なタイミングで RequestSerialization() を呼ぶ）
public class Property : UdonSharpBehaviour
{
    // =====================================================================
    // 基本情報
    // =====================================================================

    public int propertyId;   // マスのID（配列やスロットで一意に管理する用）
    public string name;      // マスの名称（UI表示やログ用）

    // =====================================================================
    // 経済関連
    // =====================================================================

    public int price;        // 購入価格（所持プレイヤーのmoneyから引かれる）
    public int toll;         // 通行料（他プレイヤーが止まったときに支払う金額）

    // =====================================================================
    // 所有者管理
    // =====================================================================

    public int ownerId;      // スロット番号で管理（-1 = 未所有）

    // =====================================================================
    // エリア・増資関連
    // =====================================================================

    public int areaId;       // 同一エリアをまとめてボーナス判定に利用するID
    public int level;        // 増資レベル（自分の物件の価値を上げるなどの拡張用）
}